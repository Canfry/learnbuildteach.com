	<h2 class="text-2xl mb-2">Harassment</h2>
	<p class="mb-4">Harassment includes:</p>

	<ul class="list-disc pl-10 my-6 space-y-2">
		<li>
			Offensive comments related to gender, gender identity and expression, sexual orientation,
			disability, mental illness, neuro(a)typicality, physical appearance, body size, race, or
			religion
		</li>
		<li>
			Unwelcome comments regarding a person’s lifestyle choices and practices, including those
			related to food, health, parenting, drugs, and employment.
		</li>
		<li>Deliberate misgendering or use of ‘dead’ or rejected names</li>
		<li>
			Gratuitous or off-topic sexual images or behavior in spaces where they’re not appropriate
		</li>
		<li>
			Simulated physical contact (e.g. textual descriptions like “hug” or “backrub”) without consent
			or after a request to stop.
		</li>
		<li>Threats of violence</li>
		<li>
			Incitement of violence towards any individual, including encouraging a person to commit
			suicide or to engage in self-harm
		</li>
		<li>Deliberate intimidation</li>
		<li>Sustained disruption of discussion</li>
		<li>Unwelcome sexual attention</li>
		<li>Continued one-on-one communication after requests to cease</li>
		<li>Publication of non-harassing private communication</li>
	</ul>

	<h2 class="text-2xl mb-2">Following Tutorials and Building Projects</h2>

	<p class="mb-4">
		Following tutorials and other learning materials is core to building your skills as a developer.
		However, as you follow said tutorials, I think it’s important to remember 2 things to ensure
		that we are learning in the most effective way and giving credit to the original content
		creators.
	</p>

	<p class="mb-4">
		Always be sure to credit whoever the creator of the content is if you decide to publish a
		similar project on your Github, portfolio, etc.
	</p>

	<p class="mb-4">
		To ensure you get the most out of the material, I always recommend that you take the things you
		learn in a tutorial and apply them by building your own unique project. By building your own
		project, you internalize the things you learned and create a unique example to include on your
		portfolio.
	</p>

	<h2 class="text-2xl mb-2">Reporting</h2>

	<p class="mb-4">
		If you are being harassed, notice that someone else is being harassed, or have any other
		concerns, please contact the admins. They'll respond as promptly as they can.
	</p>

	<p class="mb-4">
		We will respect confidentiality requests for the purpose of protecting victims of abuse. At our
		discretion, we may publicly name a person about whom we’ve received harassment complaints, or
		privately warn third parties about them. We will not name harassment victims without their
		affirmative consent.
	</p>

	<h3 class="text-xl">Consequences</h3>
	<p class="mb-4">Participants asked to stop any harassing behavior are expected to comply immediately.</p>

	<p class="mb-4">
		If a participant engages in harassing behavior, the admins may take any action they deem
		appropriate, up to and including expulsion from this Discord and identifying the participant
		publicly as someone about whom we've received complaints.
	</p>

